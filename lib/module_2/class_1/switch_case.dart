

main() {
  int day = 4;
  switch (day) {
    case 1:
      print('Saturday');
    case 2:
      print('Sunday');
    case 3:
      print('Monday');
    case 4:
      print('Tuesday');
    case 5:
      print('wednesday');
    case 6:
      print('thursday');
    case 7:
      print("friday");
    default:
      print('Invalid day');
  }
}
